// Helpers
const byPos = (pos) => dictionary.filter(d => d.pos === pos)
const byPosAndLevel = (pos, maxLevel) => {
  const order = { 'Ini-1': 1, 'Inter-2': 2, 'Avanc-3': 3 }
  const max = order[maxLevel] || 3
  return dictionary.filter(d => d.pos === pos && (order[d.level] || 3) <= max)
}

export function getWordsByLevel(level) {
  if (level === 'all') return dictionary
  return dictionary.filter(w => w.level === level)
}

export function getWordsByCategory(category) {
  if (category === 'all') return dictionary
  return dictionary.filter(w => w.category === category)
}

export function getByPos(pos, maxLevel) {
  return maxLevel ? byPosAndLevel(pos, maxLevel) : byPos(pos)
}

export function getRandomWords(n = 10, filters = {}) {
  let pool = [...dictionary]
  if (filters.level && filters.level !== 'all') pool = pool.filter(w => w.level === filters.level)
  if (filters.category && filters.category !== 'all') pool = pool.filter(w => w.category === filters.category)
  if (filters.pos) pool = pool.filter(w => w.pos === filters.pos)
  const shuffled = pool.sort(() => Math.random() - 0.5)
  return shuffled.slice(0, n)
}

export function findWord(word) {
  const lower = word.toLowerCase()
  return dictionary.find(d => d.word.toLowerCase() === lower)
}
